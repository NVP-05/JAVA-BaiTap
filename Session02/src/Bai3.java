public class Bai3 {
    public static void main(String[] args) {
        int x = 1;
        int y = 2;
        System.out.println(x==y);
        System.out.println(x!=y);
        System.out.println(x>y);
        System.out.println(x<y);

        x = 2;
        y = 2;
        System.out.println(x==y);
        System.out.println(x!=y);
        System.out.println(x>y);
        System.out.println(x<y);

        x = 2;
        y = 1;
        System.out.println(x==y);
        System.out.println(x!=y);
        System.out.println(x>y);
        System.out.println(x<y);
    }
}
